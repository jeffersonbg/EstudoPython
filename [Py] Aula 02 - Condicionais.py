print(' ***CONDICIONAL if*** ')
if 2 > 1:
    print('2 é maior que 1')
print()    


print(' ***CONDICIONAL if-else*** ')    
if 5 > 1:
    print('5 é maior que 1')
else:
    print('5 é menor que 1')
print()

print(' ***CONDICIONAL elif*** ')
if 5 > 1:
    print('5 é maior que 1')
elif 5 < 1:
    print('5 é menor que 1')
else:
    print('5 é igual a 1')
print()
    
print(' ***OPERADORES DE COMPARAÇÂO*** ')
print(2 == 2)
print(2 != 2)
print(2 > 2)
print(2 < 2)
print(2 >= 2)
print(2 <= 2)
print()

print(' ***OPERADORES LOGICOS*** ')
print(2 == 2 and 2 > 1)
print(2 == 2 or 2 > 1)
print(not 2 == 2)
print()

print(' ***OPERADORES DE IDENTIDADE*** ')
print(2 is 2)
print(2 is not 2)
print()

print(' ***OPERADORES DE ASSOCIAÇÂO*** ')
lista = [1, 2, 3]
print(1 in lista)
print(1 not in lista)

